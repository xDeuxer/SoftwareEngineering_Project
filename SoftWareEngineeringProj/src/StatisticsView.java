    public void SetUserDetails(User user){
        jLabel4.setText(user.getName());
        jLabel5.setText(user.getType());
        jLabel6.setText(String.valueOf(user.getBalance()));
    }
    public void setStoreOwnerSpecificDetails(StoreOwner owner)
    {
        String[] names=owner.getStoreNames();
        DefaultComboBoxModel m=new DefaultComboBoxModel(names);
        jComboBox1.setModel(m);
        String MostViewProduct = owner.getMostViewedProduct();
        jLabel9.setText(MostViewProduct);
    }

    public void setNormalUserSpecificDetails(User user)
    {
        String[] names=user.getProductsNames();
        DefaultComboBoxModel m=new DefaultComboBoxModel(names);
        jComboBox2.setModel(m);
        jLabel11.setText(String.valueOf(user.getTotalCost()));
        
    }

